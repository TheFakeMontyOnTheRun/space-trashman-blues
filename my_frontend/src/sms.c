void show_text(int _x, int y, char *text) {

    uint8_t len = strlen(text);
    char *ptr = text;
    uint8_t c = 0;
    uint8_t chary = 0;
    uint8_t x = _x;

    for (; c < len && y < 64; ++c) {

        char cha = *ptr;

        if (cha == '\n') {
            ++y;
            x = _x;
            ++ptr;
            continue;
        }

        ++x;

        if (x >= 64) {
            ++y;
            x = _x;
        }

        if (cha != ' ') {
            uint8_t baseY = (y << 3);

            uint8_t *fontTop = &font[((cha - 32) << 3)];
            uint8_t baseX = (x << 3);

            for (chary = 0; chary < 8; ++chary) {
                baseY++;
                uint8_t ch = *fontTop;
                if (ch != 0) {
                    baseX += 7;
                    for (uint8_t r = 0; r < 7; ++r) {
                        if (ch & 1) {
                            plot(baseX, baseY);
                        }
                        --baseX;
                        ch >>= 1;
                    }
                }

                ++fontTop;
            }
        }
        ++ptr;
    }
}

