uint8_t getKey() {
    unsigned int key = read_joypad1();

    if (key & JOY_UP) {
        return 'w';
    }

    if (key & JOY_LEFT) {
        if (key & JOY_FIREB) {
            return 'a';
        }
        return 'q';
    }


    if (key & JOY_RIGHT) {
        if (key & JOY_FIREB) {
            return 'd';
        }
        return 'e';
    }

    if (key & JOY_DOWN) {
        return 's';
    }

    if (key & JOY_FIREA) {
        performAction();
        HUD_refresh();
        return 'p';
    }

    if (key & JOY_FIREB) {
        cursorPosition = (cursorPosition + 1);

        if (cursorPosition >= 7) {
            cursorPosition = 0;
        }

        HUD_refresh();
        return 'p';
    }

    return '.';
}

