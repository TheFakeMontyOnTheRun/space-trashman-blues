void graphicsFlush() {
    uint8_t *ptr = &buffer[0];
    for (uint8_t y = 0; y < 128; ++y) {
        for (uint8_t x = 0; x < 128;) {
            uint8_t pixel = *ptr;
            uint8_t r = 4;

            while (r--) {
                uint8_t twoBits = pixel & 192;

                if (twoBits == 64) {
                    plot(x, y);
                } else if (twoBits == 128) {
                    unplot(x, y);
                }
                pixel <<= 2;
                ++x;
            }

            *ptr = (*ptr << 1) & 0b10101010;
            ptr++;
        }
    }
}

void vLine(uint8_t x0, uint8_t y0, uint8_t y1) {

    uint8_t *ptr;
    uint8_t _y0 = y0;
    uint8_t _y1 = y1;
    uint8_t _x0 = x0;
    uint8_t offset;

    if (x0 >= XRES) return;

    if (y0 > y1) {
        _y0 = y1;
        _y1 = y0;
    }

    _x0 = _x0 >> 2;
    offset = (x0 & 3);
    x0 = _x0;

    ptr = &buffer[(_y0 * 32) + x0];

    if (_y1 >= YRES) {
        _y1 = YRESMINUSONE;
    };


    if (_y0 >= YRES) {
        _y0 = YRESMINUSONE;
    };

    switch (offset) {
        case 0:
            for (uint8_t y = _y0; y <= _y1; ++y) {
                *ptr |= 64;
                ptr += 32;
            }
            break;
        case 1:
            for (uint8_t y = _y0; y <= _y1; ++y) {
                *ptr |= 16;
                ptr += 32;
            }
            break;
        case 2:
            for (uint8_t y = _y0; y <= _y1; ++y) {
                *ptr |= 4;
                ptr += 32;
            }
            break;
        case 3:
            for (uint8_t y = _y0; y <= _y1; ++y) {
                *ptr |= 1;
                ptr += 32;
            }
            break;
    }
}

void graphicsPut(uint8_t x, uint8_t y) {

    uint8_t *ptr;
    uint8_t offset;

    if (y >= YRES || x >= XRES) return;

    offset = (x & 3);
    x = x >> 2;

    ptr = &buffer[(y * 32) + x];

    switch (offset) {
        case 0:
            *ptr |= 64;
            break;
        case 1:
            *ptr |= 16;
            break;
        case 2:
            *ptr |= 4;
            break;
        case 3:
            *ptr |= 1;
            break;
    }
}


