void graphicsFlush() {
	uint8_t *ptr = &buffer[0];
	for (uint8_t y = 0; y < BUFFER_RESY; y += 8) {
		uint16_t addr = map_pixel(0, y + 32);
		vwrite(ptr, addr, 16 * 8);
		ptr += 8 * 16;
	}
	memset(&buffer[0], 0, BUFFER_SIZEX * BUFFER_SIZEY);
}

