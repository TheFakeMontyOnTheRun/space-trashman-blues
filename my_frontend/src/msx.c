void writeStrWithLimit(uint8_t _x, uint8_t y, char *text, uint8_t limitX) {

	uint8_t len = strlen(text);
	char *ptr = text;
	uint8_t c = 0;
	uint8_t chary = 0;
	uint8_t x = _x;

	for (; c < len && y < 64; ++c) {

		char cha = *ptr;

		if (x == limitX) {
			++y;
			x = _x;
		} else if (cha == '\n') {
			++y;
			x = _x;
			++ptr;
			continue;
		}

		if (cha >= 'a') {
			if (cha <= 'z') {
				cha = (cha - 'a') + 'A';
			} else {
				cha -= ('z' - 'a');
			}
		}

		uint8_t baseY = (y << 3);

		uint8_t *fontTop = &font[((cha - 32) << 3)];
		vwrite(fontTop, map_pixel(x << 3, y << 3), 8);

		++x;
		++ptr;
	}
}

